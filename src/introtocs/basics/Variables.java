package introtocs.basics;


public class Variables {

	public static void main(String[] args) {
		// Working with Variables: Type Identifier Content
		String message = "Variables are awesome";
		System.out.println(message);
		
		// Reassigning Variables
		String my_message = "Variables are Fantastic!";
		my_message = "Variables are Super!";
		System.out.println(my_message);
		
		// CamelCase
		String myMessage = "Variables are Fantastic!";
		myMessage = "Variables are Super!";
		System.out.println(myMessage);
		
		// Number Variables
		int year = 2013;
		double pi = 3.14;
		System.out.println(year);
		System.out.println(pi);
		
		// Variables withing other Variables
		int five = 5;
		int ten = 10;
		int sum = five + ten;
		System.out.println(sum);

	}

}
